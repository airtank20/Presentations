-- AS OF
SELECT * FROM sales.orders_temporal
	FOR SYSTEM_TIME
		AS OF '2019-05-31 00:00:00.0000000' 
			ORDER BY orderid ASC

-- FROM
SELECT * FROM Application.People
	FOR SYSTEM_TIME
		FROM '2015-01-01 00:00:00.0000000' TO '2016-01-01 00:00:00.0000000'
			ORDER BY PersonID ASC

-- BETWEEN
SELECT * FROM Application.People
	FOR SYSTEM_TIME
		BETWEEN '2016-05-01 00:00:00.0000000' AND '2016-05-31 00:00:00.0000000'
			ORDER BY PersonID ASC

-- CONTAINED IN
SELECT * FROM Application.People
	FOR SYSTEM_TIME
		CONTAINED IN ('2016-01-01 00:00:00.0000000', '2017-01-01 00:00:00.0000000')
			ORDER BY PersonID ASC

-- ALL
SELECT * FROM Application.People
	FOR SYSTEM_TIME ALL
		ORDER BY PersonID ASC